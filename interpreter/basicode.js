{
    this.token_type = "name";
    this.payload = value;
}

function newFunctionToken(keyword, operation) {
    return (function() {
        return (new function FunctionToken() {
            this.token_type = "function";
            this.payload = keyword;
            this.operation = operation;
        }() );
    } );
}
